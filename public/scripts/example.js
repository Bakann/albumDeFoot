var Player = React.createClass({
  rawMarkup: function() {
    var rawMarkup = marked(this.props.children.toString(), {sanitize: true});
    return { __html: rawMarkup };
  },

  render: function() {
    return (
      <div className="player">
        <h2 className="playerName">
          {this.props.name}
        </h2>
        <span dangerouslySetInnerHTML={this.rawMarkup()} />
      </div>
    );
  }
});

var PlayerBox = React.createClass({
  loadPlayersFromServer: function() {
    $.ajax({
      url: this.props.url,
      dataType: 'json',
      cache: false,
      success: function(data) {
        this.setState({data: data});
      }.bind(this),
      error: function(xhr, status, err) {
        console.error(this.props.url, status, err.toString());
      }.bind(this)
    });
  },
  handlePlayerSubmit: function(player) {
    var players = this.state.data;
    //  Optimistically set an id on the new player. It will be replaced by an
    // id generated by the server. In a production application you would likely
    // not use Date.now() for this and would have a more robust system in place.
    player.id = Date.now();
    var newPlayers = players.concat([player]);
    this.setState({data: newPlayers});
    $.ajax({
      url: this.props.url,
      dataType: 'json',
      type: 'POST',
      data: player,
      success: function(data) {
        this.setState({data: data});
      }.bind(this),
      error: function(xhr, status, err) {
        this.setState({data: players});
        console.error(this.props.url, status, err.toString());
      }.bind(this)
    });
  },
  getInitialState: function() {
    return {data: []};
  },
  componentDidMount: function() {
    this.loadPlayersFromServer();
    setInterval(this.loadPlayersFromServer, this.props.pollInterval);
  },
  render: function() {
    return (
      <div className="playerBox">
        <h1>Players</h1>
        <PlayerList data={this.state.data} />
        <PlayerForm onPlayerSubmit={this.handlePlayerSubmit} />
      </div>
    );
  }
});

var PlayerList = React.createClass({
  render: function() {
    var playerNodes = this.props.data.map(function(player) {
      return (
        <Player name={player.name} key={player.id}>
          {player.quality}
        </Player>
      );
    });
    return (
      <div className="playerList">
        {playerNodes}
      </div>
    );
  }
});

var PlayerForm = React.createClass({
  getInitialState: function() {
    return {name: '', quality: ''};
  },
  handleAuthorChange: function(e) {
    this.setState({name: e.target.value});
  },
  handleTextChange: function(e) {
    this.setState({quality: e.target.value});
  },
  handleSubmit: function(e) {
    e.preventDefault();
    var name = this.state.name.trim();
    var quality = this.state.quality.trim();
    if (!quality || !name) {
      return;
    }
    this.props.onPlayerSubmit({name: name, quality: quality});
    this.setState({name: '', quality: ''});
  },
  render: function() {
    return (
      <form className="playerForm" onSubmit={this.handleSubmit}>
        <input
          type="quality"
          placeholder="Your name"
          value={this.state.name}
          onChange={this.handleAuthorChange}
        />
        <input
          type="quality"
          placeholder="Say something..."
          value={this.state.quality}
          onChange={this.handleTextChange}
        />
        <input type="submit" value="Post" />
      </form>
    );
  }
});

ReactDOM.render(
  <PlayerBox url="/api/players" pollInterval={2000} />,
  document.getElementById('content')
);
